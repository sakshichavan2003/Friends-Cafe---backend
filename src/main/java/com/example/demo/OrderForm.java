package com.example.demo;

import java.util.List;

public class OrderForm {
	
	    private List<String> hotCoffee;
	    private List<String> coldCoffee;
	    private List<String> pizzas;
	    private List<String> burgers;
	    private List<String> desserts;
	    private List<String> iceCreams;
	    private List<String> combos;
		public List<String> getHotCoffee() {
			return hotCoffee;
		}
		public void setHotCoffee(List<String> hotCoffee) {
			this.hotCoffee = hotCoffee;
		}
		public List<String> getColdCoffee() {
			return coldCoffee;
		}
		public void setColdCoffee(List<String> coldCoffee) {
			this.coldCoffee = coldCoffee;
		}
		public List<String> getPizzas() {
			return pizzas;
		}
		public void setPizzas(List<String> pizzas) {
			this.pizzas = pizzas;
		}
		public List<String> getBurgers() {
			return burgers;
		}
		public void setBurgers(List<String> burgers) {
			this.burgers = burgers;
		}
		public List<String> getDesserts() {
			return desserts;
		}
		public void setDesserts(List<String> desserts) {
			this.desserts = desserts;
		}
		public List<String> getIceCreams() {
			return iceCreams;
		}
		public void setIceCreams(List<String> iceCreams) {
			this.iceCreams = iceCreams;
		}
		public List<String> getCombos() {
			return combos;
		}
		public void setCombos(List<String> combos) {
			this.combos = combos;
		}

	    
	}

